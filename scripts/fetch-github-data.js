import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const generatedFile = path.join(rootDir, "src", "generated", "github-data.ts");
const configFile = path.join(rootDir, "src", "config.ts");

const username = await readGitHubUsername(configFile);

if (!username) {
  throw new Error("Could not determine GITHUB_USERNAME from src/config.ts");
}

const repos = await fetchAllRepos(username);
const repoData = await Promise.all(repos.map((repo) => fetchRepoDetails(username, repo)));
const keptRepoData = repoData.filter((v) => v != null);

await mkdir(path.dirname(generatedFile), { recursive: true });
await writeFile(
  generatedFile,
  [
    'import type { GitHubRepo } from "../types";',
    "",
    "// This file is generated by scripts/fetch-github-data.mjs.",
    `export const GITHUB_REPOS: GitHubRepo[] = ${JSON.stringify(keptRepoData, null, 2)};`,
    "",
  ].join("\n"),
  "utf8",
);

async function readGitHubUsername(filePath) {
  const content = await readFile(filePath, "utf8");
  const match = content.match(/export const GITHUB_USERNAME = [\"'`](.+?)[\"'`];/);
  if (match != null) {
    return match[1];
  }

  return "";
}

async function fetchAllRepos(owner) {
  const allRepos = [];
  let page = 1;

  while (true) {
    const response = await fetchGh(
      `https://api.github.com/users/${owner}/repos?sort=updated&per_page=100&page=${page}`,
    );

    if (!response.ok) {
      throw new Error(`Failed to fetch repositories for ${owner}: ${response.status}`);
    }

    const repos = await response.json();
    if (!Array.isArray(repos) || repos.length === 0) break;

    allRepos.push(...repos);
    if (repos.length < 100) break;
    page += 1;
  }

  return allRepos;
}

async function fetchRepoDetails(owner, repo) {
  const [readme, portfolioNote] = await Promise.all([
    readRepoReadme(owner, repo.name),
    readTopLevelPortfolioMd(owner, repo.name),
  ]);

  if (portfolioNote != null) {
    /// If we have a portfolio.md with the rank -1,
    ///   We explicitly opt to hide it from our portfolio.
    for (const line of portfolioNote.split("\n")) {
      const result = line.match(/\<!--\s*rank:\s*-1\s*-->/);
      if (result != null) {
        return null;
      }
    }
  }

  return {
    id: repo.id,
    name: toTitleCase(repo.name),
    description: repo.description,
    url: repo.html_url,
    homepage: repo.homepage ?? null,
    live: findGithubPagesUrl(repo.description) || repo.homepage || null,
    language: repo.language,
    readme,
    portfolioNote: removeHtmlComments(portfolioNote),
  };
}

async function readTopLevelPortfolioMd(owner, repoName) {
  try {
    /// Fetch the contents of the repository.
    const response = await fetchGh(`https://api.github.com/repos/${owner}/${repoName}/contents`);
    if (!response.ok) return null;

    /// Get each individual content.
    const items = await response.json();
    if (!Array.isArray(items)) return null;

    /// Look at the files found.
    const match = items.find(
      (i) => i?.type === "file" && i?.name?.toLowerCase() === "portfolio.md",
    );
    if (match == null) return null;

    if (match.download_url) {
      const contentResponse = await fetchGh(match.download_url);
      if (!contentResponse.ok) return null;

      return await contentResponse.text();
    }

    const contentResponse = await fetchGh(match.url);
    if (!contentResponse.ok) return null;

    const fileData = await contentResponse.json();
    if (fileData.encoding === "base64" && typeof fileData.content === "string") {
      return Buffer.from(fileData.content.replace(/\n/g, ""), "base64").toString("utf8");
    }

    return null;
  } catch {
    return null;
  }
}

async function readRepoReadme(owner, repoName) {
  try {
    const response = await fetchGh(`https://api.github.com/repos/${owner}/${repoName}/readme`);
    if (!response.ok) return null;

    const data = await response.json();
    if (data.encoding === "base64" && typeof data.content === "string") {
      return Buffer.from(data.content.replace(/\n/g, ""), "base64").toString("utf8");
    }

    return null;
  } catch {
    return null;
  }
}

function findGithubPagesUrl(text) {
  if (!text) return null;
  const urlRegex = /(https?:\/\/[^\s]+)/g;
  const matches = text.match(urlRegex);
  if (!matches) return null;
  const githubPagesUrl = matches.find((url) => url.includes("github.io"));
  return (githubPagesUrl || matches[0]).replace(/[)\.\s]+$/, "");
}

function fetchGh(url) {
  return fetch(url, { headers: githubHeaders() });
}

/// Global mutable variables run by NodeJS require var.
var _loadedHeaders = null;

function githubHeaders() {
  if (_loadedHeaders != null) {
    return _loadedHeaders;
  }

  const headers = {
    "Accept": "application/vnd.github.v3+json",
    "User-Agent": "web-portfolio-build-script",
  };

  if (process.env.GITHUB_TOKEN) {
    console.log("Found github token.");
    headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  }

  return (_loadedHeaders = headers);
}

function camelToSnakeCase(value) {
  const regex = /((?:[A-Z][a-z])|(?:(?<=[a-z])[^a-z])|(?:(?<=[A-Z])[0-9_]))/g;

  return value.toString().replace(regex, "_$1".toLowerCase());
}

function toTitleCase(value) {
  // Use camelToSnakeCase to break up camelCase words first
  const snake = camelToSnakeCase(value.toString());

  // Replace hyphens and underscores with spaces
  const spaced = snake.replace(/[-_]/g, " ");

  // Capitalize the first letter of each word
  return spaced
    .split(/\s+/)
    .map((word) => {
      if (!word) return "";
      return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
    })
    .join(" ");
}

function removeHtmlComments(string) {
  if (string == null) return null;

  return string.replace(/<!--(?:.|\n)*?-->/g, "");
}
